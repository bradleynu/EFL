<h2 class="special-bg" id="athlete-foot">Pie de Atleta</h2>
¿Tiene picazón incómoda, pies agrietados, descamación con picor, inflamación y ampollas, o ardor en sus pies? Podría estar sufriendo del pie de atleta.
<h3>¿Qué es el pie de atleta?</h3>
El pie de atleta, también conocido como tinea pedís, es una infección fúngica de la capa superior de la piel del pie, que a menudo causa erupciones, picazón y molestias. Es una de las infecciones fúngicas más comunes. Hay tres tipos de pie de atleta. Cada uno afecta diferentes partes del pie y puede verse diferente.

Sin embargo, este tipo de hongo crece casi siempre en las áreas del cuerpo que se mantienen calientes y húmedas, como por ejemplo, entre los dedos de los pies. El pie de atleta también puede desarrollarse en los laterales o la parte inferior de los pies, puede extenderse a las uñas y la ingle si se rasca la infección y luego se tocan esas partes. El pie de atleta es muy contagioso.
<h3>¿Cuáles son los síntomas del Pie de Atleta?</h3>
<ul>
   <li>Ardor de los pies.</li>
   <li>Ampollas.</li>
   <li>Grietas en los pies o dedos.</li>
   <li>Descamación en los pies.</li>
   <li>Picazón de los pies.</li>
   <li>Picor y enrojecimiento particularmente en la noche.</li>
</ul>
<h3>¿Cuáles son las causas del Pie de Atleta?</h3>
El pie de atleta es altamente contagioso y se transmite de persona a persona tocando el area infectada. Caminar descalzo en vestuarios, piscinas públicas y otras áreas comunes podría ponerlo en riesgo de desarrollar pie de atleta. Ciertos zapatos, debido a su ambiente oscuro y cálido, también crean un lugar ideal para que el hongo se desarrolle.

Aunque el pie de atleta es contagioso, hay algunas personas que son más susceptibles que otras. Por ejemplo, los hombres son más propensos al pie de atleta, al igual que las personas que ya han padecido del pie de atleta. Otros factores que pueden aumentar su riesgo incluyen la edad (más común en adultos mayores) que tienen las defensas bajas debido a las condiciones de salud tales como la diabetes o el cáncer, y que viven en un clima cálido y húmedo.
<h3>¿Cómo puedo tratar el pie de atleta?</h3>
Es recomendable que usted trate el pie de atleta al primer síntoma. La mayoría de los casos el pie de atleta puede ser curado con productos antifungales de venta libre y una buena higiene. El uso regular de una crema o polvo antifungal en la mayoría de los casos erradica la condición.

Lávese y seque bien sus pies, especialmente entre sus dedos, dos veces al día y cámbiense de medias. Polvoree un polvo antifungal como Hongo Killer® Polvo o Hongo Killer® Polvo en Spray en sus pies y en sus zapatos diariamente. Las cremas antifungales como el Hongo Killer® crema así como también en líquido como Hongo Killer® Spray, Hongo Killer® Ultra o Hongo Killer® Solución Antifungal son efectivas y deben ser utilizados en la noche. Continue este tratamiento por una o dos semanas después de que la infección se haya eliminado para prevenir una recurrencia.

También hay algunas cosas que puede hacer para prevenir el pie de atleta. Estas incluyen usar chanclas en los vestuarios y piscinas públicas, utilizar polvo para mantener los pies secos y usar sandalias para disminuir la humedad en los pies.

Si el pie de atleta no mejora en 2 a 4 semanas o reaparece con frecuencia, acuda al médico.
