<h3>What is Memory Loss?</h3>
Memory loss, is unusual forgetfulness that makes it difficult to remember a range of information.
<h3>What Are the Symptoms?</h3>
<ul>
   <li>Forgetting where you left things</li>
   <li>Forgetting names of acquaintances</li>
   <li>Having trouble recalling what you just read, or the details of a recent conversation</li>
