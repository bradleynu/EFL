<h2 class="special-bg" id="canker-sore">Afta</h2>
<h3>¿Qée es la Irritación bucal y las lesiones orales?</h3>
Las irritaciones bucales y las lesiones orales son puntos o ulceras inflamadas en la boca, labios o lengua. Aunque hay numerosos tipos de úlceras y desordenes bucales, entre las más comunes están las <b>aftas bucales</b>, llamado también <b>estomatitis</b> y el <b>herpes labial</b>, en adición también esta la <b>candidiasis</b>. Si usted sufre un afta bucal, usted no esta solo, alrededor de un tercio de la población están afectados en algún punto. No obstante, las úlceras, irritación y lesiones bucales pueden ser dolorosas, de aspecto desagradable y pueden interferir cuando usted come o habla. Cualquier úlcera que persista 1 semana o mas, debe ser examinada por un dentista o médico.
<h3>¿Cómo se si tengo una úlcera o una lesión bucal?</h3>
Los siguientes signos deben indicar una ulcera o lesión bucal.
<ul>
   <li><b>Afta Bucal/Estomatitis:</b> La ulcerita bucal, también conocida como Afta, es una simple ulcerita de color blancuzco o amarillento, rodeada de un anillo rojizo, que también pueden presentarse en un racimo o grupito de tales ulceritas. Estas ulceritas pueden estar presente en los carrillos (parte interna lateral de su boca) en la lengua o en sus labios de la parte interna de ellos. La causa de esto no esta completamente entendido, pero se piensa que esta lesión es una respuesta de la Célula T, la cual es un mediador a las respuestas del sistema inmune, que se dispara por una variedad de factores. Factores tales como: stress- traumas-alergias- fumar y algunos tipo de alimentos, además queda el factor hereditario, cualquiera de estos factores pueden hacer susceptible a la persona. El Afta (estomatitis) es una de las causas mas común en la enfermedad de la mucosa oral, lo cual se cree afecta al 20% de la población en general en algún grado. Los síntomas van, desde una simple molestia a una incapacitación que impacta el área de comer, hablar o tragar. En su forma más severa, puede ocasionar pérdida de peso.</li>
   <li><b>Herpes Labial:</b> También llamado “ampolla por fiebre” o herpes simple tipo 1. Es un grupo de ampollas dolorosas y llenas de líquido que están en los labios o alrededor de ellos y en ocasiones debajo de la nariz o alrededor de la barbilla. El herpes labial es usualmente causado por un tipo de herpes viral el cual es altamente contagioso. A diferencia del afta bucal, el herpes labial es contagioso desde el instante que una de las ampollas se rompe hasta el momento que esta se sana completamente.</li>
</ul>
<h3>¿Cómo la irritación bucal o lesión oral son tratadas?</h3>
El tratamiento varia de acuerdo al tipo de condición que usted tenga. Para los tipos mas comunes de ulceras bucales o alteración descrita antes, el tratamiento es como sigue:
<ul>
   <li><b>Afta Bucal:</b> El afta bucal usualmente se cura luego de 7 a 10 días, aunque repetición de los brotes son comunes. Los ungüentos tópicos, sin receta, y algunos anestésicos tópicos locales, proveen alivio temporal, aliviando el dolor y reduciendo la inflamación lo cual promueve la curación de la ulcerita. En el Finafta® líquido, usted tiene el antiséptico y anestésico ideal para curarle su Afta. Usar enjuagues bucales antimicrobianos, ayuda a reducir la irritación.</li>
