<h2 class="special-bg" id="canker-sore">Canker Sore</h2>
<h3>What are Mouth irritations and Oral Lesions:</h3>
Mouth irritations and oral lesions are swelling, spots or sores on your mouth, lips or tongue. Although there are numerous types of mouth sores and disorders, among the most common are canker sores (Aphthous stomatitis) and cold sores (leukoplakia and candidiasis). If you have a mouth sore, you are not alone, around one third of all people are affected at some point. Nonetheless, mouth sores, irritations and lesions can be painful, unsightly and can interfere with eating and speaking. Any mouth sore that persists for a week or more should be examined by your dentist <span style="font-weight: 400;">or physician</span>.
<h3>How do I know if I have a Mouth Sore or Oral Lesion?</h3>
The following signs may indicate a mouth sore or oral lesion.
<ul>
   <li><b>Canker sores/aphthous stomatitis:</b> Canker sore, also known as an aphthous ulcer, is a single pale or yellow ulcer with red outer ring or a cluster of such ulcers in the mouth, usually on the cheeks, tongue or inside the lips. The cause is not completely understood, but it is thought that the condition represents a T cell mediated immune response which is triggered by a variety of factors. Factors such as stress, trauma, allergies, cigarette smoking, some food and heredity may also make one susceptible. Apthous stomatitis is one of the most common diseases of the oral mucosa and is thought to affect 20% of the general population to some degree. The symptoms range from a minor nuisance, to be disabling in their impact on eating, swallowing and talking.</li>
   <li><b>Cold sores: Also called fever blisters or herpes simplex type 1</b> are a group of painful, fluid-filled blisters that occur on or around the lips and sometimes under the nose or around the chin. Cold sores are usually caused by a type of herpes virus and are contagious. Unlike canker sores, cold sores are contagious from the time the blister ruptures to the time it has completely healed.</li>
</ul>
<h3>How are Mouth irritations and Oral Lesions Treated?</h3>
Treatment varies according to the type of condition you have. For the most common types of mouth sores and disorders, described above, treatment is as follow.
<ul>
   <li><b>Canker Sores:</b> Canker sores usually heal after seven to 10 days, although recurrent outbreaks are common. Non-prescription topical ointments and pain relievers can provide temporary relief, alleviating the pain, reducing the inflammation and promoting the healing of the ulcers. Finafta® liquid gives you relief with an antiseptic and anesthetic action, which help to alleviate your canker sore. Rinsing with antimicrobial mouth rinses may help reduce the irritation.</li>
   <li><b>Cold Sores:</b> The blisters usually heal in about a week. Because there is not cure for herpes infections, the blisters may reoccur during times of emotional upset, exposure to sunlight, allergies or fever. Non-prescription topical anesthetics like Finafta® liquid can provide temporary relief. Prescription antiviral drugs may reduce these kinds of viral infections- ask your dentist or physician.</li>
</ul>
