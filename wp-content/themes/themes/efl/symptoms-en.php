<h2 class="special-bg"  id="athlete-foot">Athlete’s Foot and Jock Itch</h2>
<h3>What is Athlete’s Foot?</h3>
<p>Do you have itchy, uncomfortable, cracked feet, burning and blisters? You could be suffering from Athlete’s Foot.</p>
<p>Athlete’s Foot, also known as tinea pedis, is a fungal foot infection of the top layer of the skin, which often causes rash, itching, burning and discomfort. It is among the most common fungal infections. There are three clinical forms of Athlete’s Foot, each one affects different parts of the foot and may look different. However, this type of fungus almost always grows in areas that are warm and moist, such as in between the toes. Fungal infection can develop on the feet, as well as in the groin area, better known as jock itch.</p>
<h3>What are the Symptoms of Athlete’s Foot?</h3>
<ul>
<li>Burning feet</li>
<li>Blisters (that do not burst)</li>
<li>Cracks</li>
<li>Flaking</li>
<li>Itching and redness, particularly at night</li>
<li>Peeling</li>
<li>Scaling</li>
</ul>
<h3>What are the causes of Athlete’s Foot?</h3>
<p>Athlete’s Foot is highly contagious and can be transmitted person to person by touching infected areas. Walking barefoot in locker rooms, public pools and other common areas could put you at risk of developing Athlete’s Foot. Certain closed shoes, because of their dark and warm environment, also create an ideal place for fungal growth. Although Athlete’s Foot is contagious, some people are more susceptible than others; for instance, males are more likely to get Athlete’s Foot than women. Other factors that can increase your risk include age (more common in the elderly), and conditions such as diabetes and living in warm and/or damp climates.</p>
<h3>How Do I Treat Athlete’s Foot?</h3>
<p>It is recommended that you treat athlete’s foot at the first sign of symptoms. In most cases, athlete’s foot can be cured with over-the counter antifungal products and good hygiene. The regular use of antifungal powder or cream will, in most cases, eradicate the condition.</p>
<p>Wash and dry your feet, including areas between the toes, twice a day, change socks/stocking daily. Sprinkle antifungal powder, Hongo Killer® Powder or Hongo Killer® Spray Powder on your feet and in your shoes daily to keep your feet dry. Antifungal creams, such as Hongo Killer® cream, as well as liquid such as Hongo Killer® Spray, Hongo Killer® Antifungal Solution or Hongo Killer® Ultra are also effective and should be used at nighttime. Continue treatment for one to two weeks after the infection has cleared to prevent it from recurring.</p>
