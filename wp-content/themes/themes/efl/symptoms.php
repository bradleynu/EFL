<h2 class="special-bg" id="cold">Resfriado Común</h2>
<p>¿Sufre de tos, congestión nasal o dolor de garganta? Usted podría estar padeciendo de un resfriado común.</p>
<h3>¿Qué es un resfriado?</h3>
<p>El resfriado común es una enfermedad viral que afecta el sistema respiratorio superior. Es la infección más común en los seres humanos, haciendo que una persona sufra hasta siete resfriados por año. Los resfriados suelen durar, a lo sumo, unas pocas semanas.</p>
<h3>¿Cuáles son los síntomas de un resfriado?</h3>
<ul>
<li>Tos.</li>
<li>Congestión.</li>
<li>Picazón/Resequedad de la garganta.</li>
<li>Fiebre.</li>
<li>Dolor de cabeza.</li>
<li>Congestión nasal.</li>
<li>Estornudo.</li>
<li>Dolor de garganta.</li>
</ul>
<h3>¿Cuáles son las causas de un resfriado común?</h3>
<p>Hay más de 200 virus que causan el resfriado común. Se puede coger un resfriado por tocar a alguien o una superficie que ha sido contaminada con el virus del resfriado. Esto sucede a menudo cuando se toca el picaporte de la puerta, los teléfonos o utensilios para comer. También se puede coger un resfriado por la inhalación de secreciones de una persona que ha estornudado en el aire. Para evitar la propagación de un resfriado, asegúrese de lavarse regularmente las manos y cubrirse la boca al toser o estornudar.</p>
<h3>¿Cómo puedo tratar el resfriado común?</h3>
<p>Aunque no existe una cura para el resfriado común, hay muchos remedios diferentes para aliviar los síntomas y hacer que se sienta mejor más rápido. Beber mucha agua y otros líquidos puede ayudar a disminuir la congestión y evitar la deshidratación. La humedad de una ducha caliente o un humidificador de mesa también pueden ayudar a despejar la nariz taponada y calmar una garganta irritada. Los medicamentos para el resfriado sin receta también pueden ayudar a aliviar los síntomas. Nuestra línea de Rompe Pecho®, Mielim y Mas®, Cough Out® y Finafta® Multioral son medicamentos seguros y eficaces para tratar los síntomas como la tos, la congestión y dolor de garganta.</p>
