<h3>¿Qué es Pérdida de la Memoria?</h3>
Pérdida de la memoria, es el olvido inusual que hace difícil recordar una serie de datos. Desde donde puso las llaves a los acontecimientos del pasado, o ambos.
<h3>¿Cuáles son los síntomas de dificultad en la memoria?</h3>
<ul>
   <li>Olvidar donde dejó las cosas.</li>
   <li>Olvidar nombres de conocidos.</li>
   <li>Tener problemas en recordar lo que acaba de leer o lo detalles de una conversación reciente.</li>
   <li>Distraerse fácilmente.</li>
</ul>
<h3>¿Causa de la pérdida de la memoria?</h3>
El olvido es una afección común entre los adultos mayores. Cuando envejecemos, experimentamos cambios fisiológicos que provocan deficiencias en la función cerebral, que hace que necesitemos más tiempo para aprender y recordar información. Mientras que los lapsos de la memoria pueden ser frustrantes, por lo general no son un motivo de preocupación y por lo general no son un indicador de enfermedades graves como la demencia.

